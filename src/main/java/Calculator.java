public class Calculator {


    public Calculator(){

    }

    public int addNumber(int num1, int num2){
        return num1 + num2;
    }

    public int subtractNumber(int num1, int num2){
        return num1 - num2;
    }

    public int multiplyNumber(int num1, int num2){
        return num1 * num2;
    }

    public double divideNumber(double num1, double num2){
        return num1 / num2;
    }
}
